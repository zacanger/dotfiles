# some code

usage() {
  if [ -z "$1" ]; then
   cat <<USAGE
Usage: $self [OPTION]... LICENSE_NAME_PATTERN
Try '$self -h' for more information.
USAGE
  else
   cat <<HELP
Usage: $self [OPTION]... LICENSE_NAME_PATTERN

Description:
  Generate license files.

Example:
  $self mit

Options:
  -y YEAR
  -c COPYRIGHT_OWNER
  -o ORGANIZATION
  -p PROGRAM
  -f output file (defaults to stdout)
  -l show all available licenses
  -h display this help and exit
  -v output version information and exit
HELP
  fi
  exit 1
}

show_version() {
  echo "$self 0.1" && exit 0
}

show_licenses() {
  licenses | sort && exit 0
}

licenses() {
  sed -n 's/^LICENSE_\(.*\)=.*/\1/p' "$0"
}

license() {
  sed -n "/^LICENSE_$1/,/'/p" "$0" | sed '1d;$d'
}

replace_variables() {
  license $1 |
    sed "s/LICENSE_YEAR/$year/" |
    sed "s/LICENSE_COPYRIGHT_OWNER/$copyright_owner/" |
    sed "s/LICENSE_ORGANIZATION/$organization/" |
    sed "s/LICENSE_PROGRAM/$program/"
}

generate() {
  license="$1"
  output_filename="$2"
  if [ -z "$output_filename" ] ; then
    replace_variables $license
  else
    replace_variables $license > "$output_filename"
    echo "$output_filename generated under $license"
  fi
}

# defaults
year=$(date +'%Y')
copyright_owner=$(test -z "$COPYRIGHT_OWNER" && whoami || echo "$COPYRIGHT_OWNER")
organization=$(test ! -z "$ORGANIZATION" && echo "$ORGANIZATION")
output_filename=$(test ! -z "$LICENSE_FILENAME" && echo "$LICENSE_FILENAME")
program=""

self=$(basename $0)
while getopts "y:c:o:p:f:lhv" name; do
  case "$name" in
    y) year=$OPTARG;shift;shift;;
    c) copyright_owner=$OPTARG;shift;shift;;
    o) organization=$OPTARG;shift;shift;;
    p) program=$OPTARG;shift;shift;;
    f) output_filename=$OPTARG;shift;shift;;
    l) show_licenses;;
    v) show_version;;
    h) usage SHOW_OPTIONS;;
    *) usage;;
  esac
done

license_name=$(test -z "$1" && echo "$LICENSE" || echo "$1")
test -z "$license_name" && echo "$self: you must specify the license name" && usage && exit 2

license=$(licenses | grep -i "$license_name")
count=$(echo "$license" | wc -w)
case "$count" in
  0) echo "No license match the given pattern: $license_name" && exit 3;;
  1) generate "$license" "$output_filename";;
  *) echo "Be more specific. $count matches found:" && licenses | grep -i --color "$license_name" && exit 4;;
esac